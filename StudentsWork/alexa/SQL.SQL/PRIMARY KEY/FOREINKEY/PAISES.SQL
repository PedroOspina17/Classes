
CREATE TABLE PAISES(
    ID INT IDENTITY(1,1) PRIMARY KEY,
    NAME VARCHAR(50),
    POBLACION FLOAT 
    )

CREATE TABLE DEPARTAMENTO(
    IDDPTO INT IDENTITY(1,1) PRIMARY KEY,
    NAME VARCHAR(50),
    IdPAISES INT FOREIGN KEY REFERENCES PAISES(ID) NOT NULL
)

CREATE TABLE CIUDADES(
    IDCIUDAD INT IDENTITY(1,1) ,
    NAME VARCHAR(50) NOt NULL,
    IDDEPARTAMENTO INT FOREIGN KEY REFERENCES DEPARTAMENTO(IDDPTO) NOT NULL
	)

  